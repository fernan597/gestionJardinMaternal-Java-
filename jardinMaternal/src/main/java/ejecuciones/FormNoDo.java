    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int dni = (Integer.parseInt(tDni.getText()));
        if (controlDni(dni)) {
            NoDocente auxNoDoc = new NoDocente();
            auxNoDoc.setNbre(tNombre.getText());
            auxNoDoc.setApe(tApellido.getText());
            auxNoDoc.setDireccion(tDir.getText());
            auxNoDoc.setCategoria((String)jBoxCategoria.getSelectedItem());
            auxNoDoc.setDni(dni);
            jardin.setNoDocentes(auxNoDoc);
            jardin.ordenarNoDocentes();
            JOptionPane.showMessageDialog(null, "Cargado con exito!");
            ArrayList<NoDocente> aux = jardin.listNoDocentes();
            file.writeFileNoDocente(aux);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

