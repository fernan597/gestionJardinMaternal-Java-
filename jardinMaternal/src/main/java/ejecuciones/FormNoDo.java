    private boolean controlDni(int d) {
        for (NoDocente aux : jardin.getNoDocentes()) {
            if (aux.getDni() == d) {
                JOptionPane.showMessageDialog(null, "Esta persona con dni ya esta cargada");
                return false;
            }
        }
        return true;
    }

